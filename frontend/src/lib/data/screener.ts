/**
 * Get variable by ID
 */
export function getVariableById(id: string): Variable | undefined {
  return variables.find((v) => v.id === id)
}

/**
 * Get variables by category
 */
export function getVariablesByCategory(category: string): Variable[] {
  return variables.filter((v) => v.category === category)
}
